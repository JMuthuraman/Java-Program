package sampleprogram;

public class Foreach {
	public static void main(String[] args) {
		int array[]= {7,4,5};
		char arry[]={'J'};
		String arr[]={"muthu"};
		for(int i:array)
		{
			System.out.println(i);
		}
		for(char k:arry)
		{
			System.out.print(k+ " ");
		}
		for(String p:arr)
		{
			System.out.print(p);
		}
			
	}

}

